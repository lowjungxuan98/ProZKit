import UIKit

@MainActor
private var loaded = false

@MainActor
private func load() {
    if loaded { return }
    loaded = true
    // Use Bundle.module to locate the font resource
    guard let fontURL = Bundle.module.url(forResource: "ionicons", withExtension: "ttf") else {
        fatalError("ionicons.ttf not found in bundle!")
    }
    guard let inData = try? Data(contentsOf: fontURL) else {
        fatalError("Failed to load data from ionicons.ttf!")
    }
    var error: Unmanaged<CFError>?
    guard let provider = CGDataProvider(data: inData as CFData) else {
        fatalError("Could not create CGDataProvider!")
    }
    guard let font = CGFont(provider) else {
        fatalError("Could not create CGFont!")
    }
    if !CTFontManagerRegisterGraphicsFont(font, &error) {
        let errorDescription = CFErrorCopyDescription(error!.takeRetainedValue())
        NSLog("Failed to load font: %@", errorDescription as String? ?? "unknown error")
    }
}

public enum Ionicons: UInt16, CustomStringConvertible {
    @MainActor
    public func label(_ size: CGFloat, color: UIColor = UIColor.black) -> UILabel {
        load()
        let label = UILabel()
        label.font = UIFont(name: "ionicons", size: size)
        label.text = description
        label.textColor = color
        label.textAlignment = .center
        label.backgroundColor = UIColor.clear
        label.frame = CGRect(x: 0, y: 0, width: size, height: size)
        label.accessibilityElementsHidden = true
        return label
    }

    @MainActor
    public func image(_ size: CGFloat, color: UIColor = UIColor.black) -> UIImage {
        let label = self.label(size, color: color)
        UIGraphicsBeginImageContextWithOptions(label.bounds.size, false, UIScreen.main.scale)
        label.layer.render(in: UIGraphicsGetCurrentContext()!)
        let image = UIGraphicsGetImageFromCurrentImageContext()
        UIGraphicsEndImageContext()
        return image!
    }

